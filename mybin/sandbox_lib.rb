#!/usr/bin/env ruby
#################################################################################
def get_wc_url_fn
  %x[svn info | gawk "/^URL: / { print $2 }"].chomp
end
#
puts "The cwd svn URL is #{get_wc_url_fn}"  
#################################################################################


#################################################################################
def get_wc_rev_fn
  %x[svn info | gawk "/^Revision: / { print $2 }"].chomp
end
#
puts "The rev #{get_wc_rev_fn}"
#################################################################################
